CREATE SEQUENCE SEQ_RESULT;

CREATE TABLE TB_RESULT(
    RESULTID NUMERIC(20),
    RACEID NUMERIC(20) NOT NULL,
    DRIVERID NUMERIC(20) NOT NULL,
    CONSTRUCTORID NUMERIC(20) NOT NULL,
    NUMBER NUMERIC(10),
    GRID NUMERIC(10) NOT NULL,
    POSITION NUMERIC(20),
    POSITIONTEXT VARCHAR(255) NOT NULL,
    POSITIONORDER NUMERIC(20) NOT NULL,
    POINTS NUMERIC(20) NOT NULL,
    LAPS NUMERIC(20) NOT NULL,
    TIME VARCHAR(50),
    MILLISECONDS NUMERIC(20),
    FASTESTLAP NUMERIC(20),
    RANK NUMERIC(20),
    FASTESTLAPTIME TIME,
    FASTESTLAPSPEED NUMERIC(20),
    STATUS NUMERIC(20) NOT NULL,
    CONSTRAINT PK_RESULT PRIMARY KEY (RESULTID),
    CONSTRAINT FK_RESULT_RACE FOREIGN KEY (RACEID) REFERENCES TB_RACE (RACEID),
    CONSTRAINT FK_RESULT_DRIVER FOREIGN KEY (DRIVERID) REFERENCES TB_DRIVER (DRIVERID),
    CONSTRAINT FK_RESULT_CONSTRUCTOR FOREIGN KEY (CONSTRUCTORID) REFERENCES TB_CONSTRUCTOR (CONSTRUCTORID),
    CONSTRAINT FK_RESULT_STATUS FOREIGN KEY (STATUS) REFERENCES TB_STATUS (STATUSID)

);


